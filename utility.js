function sendTo(site, id)
{
	var value = document.getElementById(id).value;
	
	// Twitter
	if (site == 1)
	{
		var url = "http://twitter.com/timeline/home?status=" + value;
	}
	
	// Facebook
	if (site == 2)
	{
		var url = "http://www.facebook.com/sharer.php?u=" + value + "&amp;t=" + encodeURIComponent(value);
	}
	
	window.open(url);
	
}

